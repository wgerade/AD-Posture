function Copy-ADPostureRedactedAclRow {
    param(
        [Parameter(Mandatory)]
        $Row
    )

    $redactedFields = @(
        'TargetName',
        'TargetDistinguishedName',
        'TargetCanonicalName',
        'TargetObjectSid',
        'TargetObjectGuid',
        'TrusteeName',
        'TrusteeSid',
        'TrusteeDistinguishedName',
        'RawTrustee',
        'OwnerName',
        'OwnerSid',
        'OwnerDistinguishedName',
        'SourceDescriptorId',
        'RawSddl',
        'Sddl',
        'RawSecurityDescriptor',
        'SecurityDescriptorSddl',
        'RelatedObjectName',
        'Path'
    )

    $copy = [ordered]@{}
    foreach ($property in $Row.PSObject.Properties) {
        $copy[$property.Name] = if ($redactedFields -contains $property.Name) { '[REDACTED]' } else { $property.Value }
    }

    [pscustomobject]$copy
}

function Copy-ADPostureDashboardAclRow {
    param(
        [Parameter(Mandatory)]
        $Row,
        [int]$EffectiveTrusteeSampleSize = 10
    )

    $copy = [ordered]@{}
    foreach ($property in $Row.PSObject.Properties) {
        if ($property.Name -in @('EffectiveTrustees', 'EffectiveMembers')) { continue }
        $copy[$property.Name] = $property.Value
    }

    $effectiveTrustees = @()
    foreach ($propertyName in @('EffectiveTrustees', 'EffectiveMembers')) {
        if ($Row.PSObject.Properties[$propertyName] -and $Row.$propertyName) {
            $effectiveTrustees += @($Row.$propertyName)
        }
    }

    $copy['EffectiveTrusteeCount'] = @($effectiveTrustees).Count
    if ($effectiveTrustees.Count -gt 0) {
        $copy['EffectiveTrusteesSample'] = @($effectiveTrustees |
            Select-Object -First $EffectiveTrusteeSampleSize |
            ForEach-Object {
                [pscustomobject]@{
                    Name = if ($_.Name) { [string]$_.Name } elseif ($_.SamAccountName) { [string]$_.SamAccountName } else { 'Unknown effective trustee' }
                    Sid = if ($_.Sid) { [string]$_.Sid } elseif ($_.ObjectSid) { [string]$_.ObjectSid } else { $null }
                    DistinguishedName = if ($_.DistinguishedName) { [string]$_.DistinguishedName } else { $null }
                    ObjectClass = if ($_.ObjectClass) { [string]$_.ObjectClass } elseif ($_.AccountType) { [string]$_.AccountType } else { $null }
                    NestingDepth = if ($null -ne $_.NestingDepth) { [int]$_.NestingDepth } else { $null }
                    Path = if ($_.Path) { [string]$_.Path } else { $null }
                }
            })
        $copy['EffectiveTrusteesTruncated'] = ($effectiveTrustees.Count -gt $EffectiveTrusteeSampleSize)
    }

    [pscustomobject]$copy
}

function Get-ADPostureDashboardPayload {
    [CmdletBinding()]
    param(
        [Parameter(Mandatory)]
        $Snapshot,
        [switch]$RedactSensitiveAclEvidence
    )

    $aclFindings = @($Snapshot.AclFindings | ForEach-Object { Copy-ADPostureDashboardAclRow -Row $_ })
    $objectEvidence = @($Snapshot.ObjectEvidence)
    if ($RedactSensitiveAclEvidence) {
        $aclFindings = @($aclFindings | ForEach-Object { Copy-ADPostureRedactedAclRow -Row $_ })
        $objectEvidence = @($objectEvidence | ForEach-Object {
            if ($_.SourceDomain -eq 'ACL' -or $_.AclFindingId) { Copy-ADPostureRedactedAclRow -Row $_ } else { $_ }
        })
    }

    $approvedExceptions = @(
        @($Snapshot.Findings) + @($Snapshot.AclFindings) + @($Snapshot.GpoFindings) + @($Snapshot.AdcsFindings) + @($Snapshot.KerberosAuthFindings) + @($Snapshot.TrustFindings) + @($Snapshot.DnsFindings) + @($Snapshot.IdentityRiskFindings) |
            Where-Object { $_.IsApprovedException }
    )

    @{
        meta = @{
            sensitivity      = 'Sensitive - contains AD posture topology, DNs, SIDs, account metadata, and remediation context'
            aclFieldClassification = Get-ADPostureAclFieldClassification
            redactedAclEvidence = [bool]$RedactSensitiveAclEvidence
            domain           = $Snapshot.Domain
            forest           = $Snapshot.Forest
            timestamp        = $Snapshot.Timestamp
            overallRiskScore = $Snapshot.OverallRiskScore
            targetScore      = $Snapshot.TargetScore
            actionableCount  = $Snapshot.ActionableCount
            approvedExceptionCount = $Snapshot.ApprovedExceptionCount
            expiredExceptionCount = $Snapshot.ExpiredExceptionCount
            readiness       = $Snapshot.ReadinessScorecard
            remediation      = $Snapshot.RemediationBreakdown
            tierBreakdown    = $Snapshot.TierBreakdown
        }
        groups   = @($Snapshot.GroupSummaries)
        findings = @($Snapshot.Findings | Where-Object { -not $_.IsExcluded })
        aclFindings = @($aclFindings)
        gpos = @($Snapshot.Gpos)
        gpoLinks = @($Snapshot.GpoLinks)
        gpoFindings = @($Snapshot.GpoFindings)
        adcsTemplates = @($Snapshot.AdcsTemplates)
        adcsCas = @($Snapshot.AdcsCas)
        adcsNtAuth = $Snapshot.AdcsNtAuth
        adcsFindings = @($Snapshot.AdcsFindings)
        kerberosAuthPrincipals = @($Snapshot.KerberosAuthPrincipals)
        kerberosAuthPolicy = $Snapshot.KerberosAuthPolicy
        kerberosAuthFindings = @($Snapshot.KerberosAuthFindings)
        trusts = @($Snapshot.Trusts)
        trustFindings = @($Snapshot.TrustFindings)
        dnsZones = @($Snapshot.DnsZones)
        dnsRecords = @($Snapshot.DnsRecords)
        dnsAdmins = @($Snapshot.DnsAdmins)
        dnsFindings = @($Snapshot.DnsFindings)
        identityRiskFindings = @($Snapshot.IdentityRiskFindings)
        monitoring = @($Snapshot.Findings | Where-Object { $_.IsExcluded -and -not $_.IsApprovedException })
        exceptions = @($approvedExceptions)
        objects = @($Snapshot.Objects)
        objectEvidence = @($objectEvidence)
        objectRelationships = @($Snapshot.ObjectRelationships)
    }
}
