function Write-ADPostureDashboardData {
    [CmdletBinding()]
    param(
        [Parameter(Mandatory)]
        $DashboardData,
        [string]$SerializedJson
    )

    $cfg = Get-ModuleConfig
    $json = $SerializedJson
    if ([string]::IsNullOrWhiteSpace($json)) {
        Write-Host '  Building compressed latest dashboard JSON...'
        $json = $DashboardData | ConvertTo-Json -Depth 12 -Compress
    }

    $reportsLatest = Join-Path $cfg.ReportPath 'latest-dashboard.json'
    $dashboardLatest = Join-Path $cfg.DashboardPath 'latest-dashboard.json'
    $dashboardJs = Join-Path $cfg.DashboardPath 'dashboard-data.js'

    if (-not (Test-Path $cfg.ReportPath)) {
        New-Item -ItemType Directory -Path $cfg.ReportPath -Force -ErrorAction Stop | Out-Null
    }

    Write-Host '  Writing latest dashboard JSON files...'
    Write-ADPostureAtomicTextFile -Path $reportsLatest -Value $json
    Write-ADPostureAtomicTextFile -Path $dashboardLatest -Value $json
    Protect-ADPostureSensitiveFile -Path $reportsLatest
    Protect-ADPostureSensitiveFile -Path $dashboardLatest
    Write-ADPostureDashboardStore -DashboardData $DashboardData -StorePath (Join-Path $cfg.ReportPath 'dashboard-store')

    $js = @"
/* Generated by AD Posture - do not edit.
   Classification: Sensitive AD posture data. Do not publish or commit. */
window.__AD_AUDIT_DATA__ = $json;
"@
    Write-Host '  Writing latest dashboard JavaScript data file...'
    Write-ADPostureAtomicTextFile -Path $dashboardJs -Value $js
    Protect-ADPostureSensitiveFile -Path $dashboardJs
}

function Write-ADPostureDashboardStore {
    [CmdletBinding()]
    param(
        [Parameter(Mandatory)]
        $DashboardData,
        [Parameter(Mandatory)]
        [string]$StorePath
    )

    if (-not (Test-Path -LiteralPath $StorePath)) {
        New-Item -ItemType Directory -Path $StorePath -Force -ErrorAction Stop | Out-Null
    }

    $collectionNames = @(
        'findings', 'monitoring', 'exceptions', 'groups',
        'objects', 'objectEvidence', 'objectRelationships',
        'aclFindings', 'gpos', 'gpoLinks', 'gpoFindings',
        'adcsTemplates', 'adcsCas', 'adcsFindings',
        'kerberosAuthPrincipals', 'kerberosAuthFindings',
        'trusts', 'trustFindings', 'dnsZones', 'dnsRecords', 'dnsAdmins', 'dnsFindings',
        'identityRiskFindings'
    )
    $manifestCollections = [ordered]@{}
    foreach ($name in $collectionNames) {
        $items = if ($DashboardData -is [System.Collections.IDictionary] -and $DashboardData.Contains($name)) {
            @($DashboardData[$name])
        }
        elseif ($DashboardData.PSObject.Properties[$name]) {
            @($DashboardData.$name)
        }
        else {
            @()
        }
        $fileName = "$name.json"
        $path = Join-Path $StorePath $fileName
        Write-ADPostureAtomicTextFile -Path $path -Value ($items | ConvertTo-Json -Depth 12 -Compress)
        Protect-ADPostureSensitiveFile -Path $path
        $manifestCollections[$name] = [ordered]@{ file = $fileName; count = @($items).Count }
    }

    $meta = if ($DashboardData -is [System.Collections.IDictionary]) { $DashboardData['meta'] } else { $DashboardData.meta }
    $adcsNtAuth = if ($DashboardData -is [System.Collections.IDictionary]) { $DashboardData['adcsNtAuth'] } else { $DashboardData.adcsNtAuth }
    $kerberosAuthPolicy = if ($DashboardData -is [System.Collections.IDictionary]) { $DashboardData['kerberosAuthPolicy'] } else { $DashboardData.kerberosAuthPolicy }
    $manifest = [ordered]@{
        version = 2
        generatedAt = (Get-Date).ToString('o')
        meta = $meta
        singletons = [ordered]@{
            adcsNtAuth = $adcsNtAuth
            kerberosAuthPolicy = $kerberosAuthPolicy
        }
        collections = $manifestCollections
    }
    $manifestPath = Join-Path $StorePath 'manifest.json'
    Write-ADPostureAtomicTextFile -Path $manifestPath -Value ($manifest | ConvertTo-Json -Depth 12 -Compress)
    Protect-ADPostureSensitiveFile -Path $manifestPath
}

